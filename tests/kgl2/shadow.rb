def test_hv(test_id, width, height, x1, x2, y, expected_data_with_soft_shadows)
  test_id = test_id.to_s

  val5 = [(0.0625 * 0xff).floor, (0.0625 * 0xff).ceil]
  val4 = [(0.125 * 0xff).floor, (0.125 * 0xff).ceil]
  val3 = [(0.25 * 0xff).floor, (0.25 * 0xff).ceil]
  val2 = [(0.5 * 0xff).floor, (0.5 * 0xff).ceil]
  val1 = [(0.75 * 0xff).floor, (0.75 * 0xff).ceil]

  shader_status_expected = y >= 0 && y < height && y != height / 2 ? 1 : 111

  bitmap = Bitmap.new(width, height)
  bitmap.fill_rect(bitmap.rect, Color.new(0xff, 0xff, 0xff, 0xff))
  bind_status = Win32API.new('System/KGL2.klib', 'kglBindShadowbuffer', 'i', 'i').call(bitmap.object_id)
  setting_status = Win32API.new('System/KGL2.klib', 'kglSoftShadows', 'i', 'i').call(0)
  shader_status = Win32API.new('System/KGL2.klib', 'kglShadowShaderH', 'iii', 'i').call(x1, x2, y)
  bitmap_data = bitmap.raw_data
  bitmap_matches_expected = true
  for r in 0...height
    for c in 0...width
      red = bitmap_data[4 * (width * r + c)].ord
      green = bitmap_data[4 * (width * r + c) + 1].ord
      blue = bitmap_data[4 * (width * r + c) + 2].ord
      alpha = bitmap_data[4 * (width * r + c) + 3].ord
      case expected_data_with_soft_shadows[width * r + c]
      when '#'
        bitmap_matches_expected = false if red != 0x00 || green != 0x00 || blue != 0x00 || alpha != 0x00
      else
        bitmap_matches_expected = false if red != 0xff || green != 0xff || blue != 0xff || alpha != 0xff
      end
    end
  end
  if setting_status == 1 && bind_status == 1 && shader_status == shader_status_expected && bitmap_matches_expected
    System.puts "Test #{test_id}a ok"
  else
    System.puts "Test #{test_id}a failed"
  end

